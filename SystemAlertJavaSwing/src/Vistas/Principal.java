    private void BTNAgregarAlarmaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BTNAgregarAlarmaMouseClicked
        
        Datos d = new Datos();
        
        d.setNombreMedicina(TXTMedicina.getText());
        d.setHora(Integer.valueOf(SpinnerHora.getValue().toString()));
        d.setMinutos(Integer.valueOf(SpinnerMinutos.getValue().toString()));
        d.setDescrpcion(TXTAREADescripcion.getText());
      
        datos.add(d);
       
        JOptionPane.showMessageDialog(null, "datos guardados");
        
        
    }//GEN-LAST:event_BTNAgregarAlarmaMouseClicked

