    private void BTNIngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BTNIngresarMouseClicked
        try {
            String usuario,contrasena, leerLinea;
            int conta = 0;
            usuario     = TXTusuario.getText();
            contrasena  = TXTContrasena.getText();
            BufferedReader leerTXT = new BufferedReader(new FileReader("Usuario.txt"));

            System.out.println("-------------------------------------------------------------------------------------");
            System.out.println("DNI      Nombre       ApePaterno     Apematerno      edad      Usuario    Contrase単a ");
            System.out.println("-------------------------------------------------------------------------------------");
             
            while ((leerLinea = leerTXT.readLine())!= null) {

                StringTokenizer st = new StringTokenizer(leerLinea, ",");
               
                if (leerLinea.contains(usuario)&& leerLinea.contains(contrasena) ) {
                    System.out.println(st.nextToken() + " " + st.nextToken() + "   " + st.nextToken() + " " + st.nextToken() + "   " + st.nextToken() + "   " + st.nextToken() + "   " + st.nextToken());
                    conta++;
                }
            }
            if (conta > 0) {
                this.setVisible(false);
                Principal pr = new Principal(usuario);
                pr.setVisible(true);
               
            } else {
                JOptionPane.showMessageDialog(null, "DNI no existe");

            }

        } catch (FileNotFoundException ex) {
            System.out.println("problema al leer el archivo de datos");
        } catch (IOException ex) {
            java.util.logging.Logger.getLogger(nuevoUsuario.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_BTNIngresarMouseClicked

